-- Every tenant-data table receives two restrictive policies:
--  * opted-in MFA users need an AAL2 JWT even when calling the Data API directly;
--  * a recovered account cannot read or mutate business data until a new factor
--    is verified and the recovery lock is completed.
do $$
declare
    tenant_table text;
begin
    foreach tenant_table in array array[
        'company_settings',
        'customers',
        'saved_items',
        'invoices',
        'recurring_templates',
        'audit_events'
    ] loop
        execute format('drop policy if exists %I on public.%I',
            'MFA assurance required for opted-in accounts', tenant_table);
        execute format($policy$
            create policy %I on public.%I
            as restrictive
            for all
            to authenticated
            using (
                array[(select auth.jwt()->>'aal')] <@ (
                    select case
                        when count(id) > 0 then array['aal2']
                        else array['aal1', 'aal2']
                    end
                    from auth.mfa_factors
                    where user_id = (select auth.uid())
                      and status = 'verified'
                )
            )
            with check (
                array[(select auth.jwt()->>'aal')] <@ (
                    select case
                        when count(id) > 0 then array['aal2']
                        else array['aal1', 'aal2']
                    end
                    from auth.mfa_factors
                    where user_id = (select auth.uid())
                      and status = 'verified'
                )
            )
        $policy$, 'MFA assurance required for opted-in accounts', tenant_table);

        execute format('drop policy if exists %I on public.%I',
            'MFA recovery must be completed', tenant_table);
        execute format($policy$
            create policy %I on public.%I
            as restrictive
            for all
            to authenticated
            using (
                not exists (
                    select 1
                    from public.mfa_recovery_state
                    where user_id = (select auth.uid())
                      and recovery_required = true
                )
            )
            with check (
                not exists (
                    select 1
                    from public.mfa_recovery_state
                    where user_id = (select auth.uid())
                      and recovery_required = true
                )
            )
        $policy$, 'MFA recovery must be completed', tenant_table);
    end loop;
end;
$$;

comment on table public.mfa_recovery_state is
    'Minimal state for one-time MFA recovery and forced TOTP re-enrolment.';
comment on table public.mfa_recovery_codes is
    'Server-only HMAC-SHA256 recovery-code values; raw codes are never stored.';
